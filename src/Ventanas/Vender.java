    private void VenderMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_VenderMouseClicked
       JTable tablaProd = productoVender.getTablaProd();
       Object[][] datos = obtenerDatosDeTabla(tablaProd);
       String InsertVenta = "insert into Ventas (fecha_venta, metodo_pago) values (?,?,?)";
       String InsertVenta_prod = "Insert into Venta_prod (id_producto, id_venta, cantidad) values (?,?,?)";
       String ActualizarStock = "update Productos stock = stock - ? where idProducto = ?";
       String IdProductoSelect ="select IdProducto from Productos where Producto = ?";
       Conectar conecta = new Conectar();
       try(Connection con = conecta.getConexion();){
           con.setAutoCommit(false);
           
           PreparedStatement stmt = con.prepareStatement(InsertVenta);
           stmt.setString(1, fecha.getText());
           stmt.setString(2, (String) mpago.getSelectedItem());
           
           ResultSet generatedKeys = stmt.getGeneratedKeys();
            int idVenta = 0;
            if (generatedKeys.next()) {
                idVenta = generatedKeys.getInt(1);
            }
           
           
           TableModel modelo = tablaProd.getModel();
           int rowCount = modelo.getRowCount();
           
           for (int i = 0; i < rowCount; i++) {
                String Producto = (String) modelo.getValueAt(i, 0);
                int cantidad = (Integer) modelo.getValueAt(i, 1);
                PreparedStatement idProdStmt = con.prepareStatement(IdProductoSelect);
                idProdStmt.setString(1, Producto);
                ResultSet rs = idProdStmt.executeQuery();
                
                int idProducto = 0;
                if (rs.next()) {
                    idProducto = rs.getInt("idProductos");
                }
                rs.close();
                idProdStmt.close();
                
                PreparedStatement stmtt = con.prepareStatement(InsertVenta_prod);
                stmtt.setInt(1, idProducto);
                stmtt.setInt(2, idVenta);
                stmtt.setInt(3, cantidad);
                
                PreparedStatement actualizar = con.prepareStatement(ActualizarStock);
                actualizar.setInt(1, cantidad);
           }                                 
       }
       catch(SQLException e){
           e.printStackTrace();
       }
    }//GEN-LAST:event_VenderMouseClicked

